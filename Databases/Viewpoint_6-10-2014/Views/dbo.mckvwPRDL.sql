CREATE VIEW dbo.mckvwPRDL
AS
SELECT     TOP (100) PERCENT dl.PRCo, dl.DLCode, dl.DLType, dl.Description,
                          (SELECT     DisplayValue
                            FROM          dbo.DDCI
                            WHERE      (dl.CalcCategory = DatabaseValue) AND (ComboType = 'PRCalcCat')) AS CalcCategory,
                          (SELECT     DisplayValue
                            FROM          dbo.DDCI AS DDCI_1
                            WHERE      (dl.Method = DatabaseValue) AND (ComboType = 'PRDLMethods')) AS Method, rm.Description AS Routine
FROM         dbo.PRDL AS dl LEFT OUTER JOIN
                      dbo.PRRM AS rm ON dl.PRCo = rm.PRCo AND dl.Routine = rm.Routine
GO
