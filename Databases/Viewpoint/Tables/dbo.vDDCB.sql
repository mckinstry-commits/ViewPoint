CREATE trigger [dbo].[vtDDCBu] on [dbo].[vDDCB] for UPDATE
/************************************
* Created: MJ 1/11/04
* Modified: 
*
* Update trigger on vDDCB (DD ComboTypes)
*
* Rejects update if any of the following conditions exist:
*	Change ComboType name
*
* Adds DD Audit entries for changed values
*
************************************/

as


declare @errmsg varchar(255), @numrows int, @validcnt int 
  
select @numrows = @@rowcount
if @numrows = 0 return
set nocount on
  
-- check for key changes 
select @validcnt = count(*) from inserted i join deleted d on i.ComboType = d.ComboType
if @validcnt <> @numrows
	begin
  	select @errmsg = 'Cannot change ComboType'
  	goto error
  	end
  	
-- DD Audit
if update(Description)
	insert dbo.vDDDA(TableName, Action, KeyString, FieldName,
		OldValue, NewValue, RevDate, UserName, HostName)
	select  'vDDCB', 'U', 'ComboType: ' + rtrim(i.ComboType), 'Description',
		d.Description, i.Description, getdate(), SUSER_SNAME(), host_name()
  	from inserted i
  	join deleted d on i.ComboType = d.ComboType 
  	where isnull(i.Description,'') <> isnull(d.Description,'')

return
  
error:
	select @errmsg = isnull(@errmsg,'') + ' - cannot update ComboType!'
    RAISERROR(@errmsg, 11, -1);
    rollback transaction
  
  
  
  
 





GO
SET QUOTED_IDENTIFIER ON
GO
SET ANSI_NULLS ON
GO
