CREATE   trigger [dbo].[vtDDCBd] on [dbo].[vDDCB] for DELETE
/************************************
* Created: MJ 1/11/05
* Modified: 
*
* Delete trigger on vDDCB (DD ComboTypes)
*
* Rejects deletion if any of the following conditions exist:
*	ComboItems (DDCI) Exist
*	Exists in a DDFI entry
*
* Adds DD Audit entry
*
************************************/
as



declare @errmsg varchar(255)
  
if @@rowcount = 0 return
set nocount on

-- check ComboTypes
if exists(select top 1 1 from deleted d join dbo.vDDCI i (nolock) on d.ComboType = i.ComboType)
	begin
  	select @errmsg = 'ComboType Items exist (vDDCI)'
  	goto error
  	end
  
-- check Form Inputs 
if exists (select top 1 1 from deleted d join dbo.vDDFI i (nolock) on d.ComboType = i.ComboType)
  	begin
  	select @errmsg = 'In use on Form Inputs (vDDFI)'
  	goto error
  	end
  	
-- DD Audit 
insert dbo.vDDDA (TableName, Action, KeyString, FieldName,
	OldValue, NewValue, RevDate, UserName, HostName)
select 'vDDCB', 'D', 'ComboType: ' + rtrim(ComboType), null,
	null, null, getdate(), SUSER_SNAME(), host_name()
from deleted
  
return
  
error:
	select @errmsg = isnull(@errmsg,'') + ' - cannot delete ComboType!'
    RAISERROR(@errmsg, 11, -1);
    rollback transaction
  
  
  
  
 







GO
SET QUOTED_IDENTIFIER ON
GO
SET ANSI_NULLS ON
GO
